   // Variabel global listener
   private DocumentListener listener;

    // Method untuk mengatur apakah hitung otomatis aktif atau tidak
    private void setAutoCount(boolean aktif) {
        if (listener != null) {
            textArea.getDocument().removeDocumentListener(listener);
            listener = null;
        }

        if (aktif) {
            listener = new DocumentListener() {
                @Override
                public void insertUpdate(DocumentEvent e) {
                    hitungStatistik();
                }

                @Override
                public void removeUpdate(DocumentEvent e) {
                    hitungStatistik();
                }

                @Override
                public void changedUpdate(DocumentEvent e) {
                    hitungStatistik();
                }
            };
            textArea.getDocument().addDocumentListener(listener);
        }
    }

    
    
   // Method untuk setup DocumentListener
    private void setupDocumentListener(boolean aktif) {
        if (listener != null) {
            textArea.getDocument().removeDocumentListener(listener);
            listener = null;
        }

        if (aktif) {
            listener = new DocumentListener() {
                @Override
                public void insertUpdate(DocumentEvent e) {
                    hitungStatistik();
                }

                @Override
                public void removeUpdate(DocumentEvent e) {
                    hitungStatistik();
                }

                @Override
                public void changedUpdate(DocumentEvent e) {
                    hitungStatistik();
                }
            };
            textArea.getDocument().addDocumentListener(listener);
        }
    }


    // Method untuk menghitung statistik
    private void hitungStatistik() {
        String teks = textArea.getText().trim();
        
        // Hitung jumlah kata
        int jumlahKata = teks.isEmpty() ? 0 : teks.split("\\s+").length;
        
        // Hitung jumlah karakter (termasuk spasi)
        int jumlahKarakter = teks.length();
        
        // Hitung jumlah karakter (tanpa spasi)
        int jumlahKarakterTanpaSpasi = teks.replaceAll("\\s+", "").length();
        
        // Hitung jumlah kalimat
        int jumlahKalimat = teks.isEmpty() ? 0 : teks.split("[.!?]+").length;
        
        // Hitung jumlah paragraf
        int jumlahParagraf = teks.isEmpty() ? 0 : teks.split("\n\n").length;
        if (jumlahParagraf == 0 && !teks.isEmpty()) jumlahParagraf = 1;

        // Update label
        kataLabel.setText("Jumlah Kata: " + jumlahKata);
        karakterLabel.setText("Jumlah Karakter: " + jumlahKarakter + " (tanpa spasi: " + jumlahKarakterTanpaSpasi + ")");
        kalimatLabel.setText("Jumlah Kalimat: " + jumlahKalimat);
        paragrafLabel.setText("Jumlah Paragraf: " + jumlahParagraf);
    }

    // Method untuk mencari kata
    private void cariKata() {
        String kataDicari = pencarianField.getText().trim();
        String teks = textArea.getText();
        
        if (kataDicari.isEmpty()) {
            hasilPencarianLabel.setText("Hasil Pencarian: Masukkan kata yang dicari");
            return;
        }

        // Hitung kemunculan kata
        int jumlahKemunculan = 0;
        if (!teks.isEmpty()) {
            String[] kataArray = teks.split("\\s+");
            for (String kata : kataArray) {
                if (kata.equalsIgnoreCase(kataDicari)) {
                    jumlahKemunculan++;
                }
            }
        }

        hasilPencarianLabel.setText("Hasil Pencarian: '" + kataDicari + "' ditemukan " + jumlahKemunculan + " kali");
    }

    // Method untuk menyimpan ke file
    private void simpanKeFile() {
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Simpan Teks dan Hasil");

        int userSelection = fileChooser.showSaveDialog(this);
        
        if (userSelection == JFileChooser.APPROVE_OPTION) {
            try {
                String filePath = fileChooser.getSelectedFile().getAbsolutePath();
                if (!filePath.endsWith(".txt")) {
                    filePath += ".txt";
                }

                BufferedWriter writer = new BufferedWriter(new FileWriter(filePath));
                
                // Tulis teks asli
                writer.write("=== TEKS ASLI ===\n");
                writer.write(textArea.getText());
                writer.write("\n\n=== HASIL PERHITUNGAN ===\n");
                writer.write(kataLabel.getText() + "\n");
                writer.write(karakterLabel.getText() + "\n");
                writer.write(kalimatLabel.getText() + "\n");
                writer.write(paragrafLabel.getText() + "\n");
                writer.write(hasilPencarianLabel.getText() + "\n");
                writer.write("\n=== BRAINMATICS - IImukonputer.Com ===\n");
                
                writer.close();
                
                JOptionPane.showMessageDialog(this, 
                    "File berhasil disimpan di: " + filePath, 
                    "Sukses", 
                    JOptionPane.INFORMATION_MESSAGE);
                    
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, 
                    "Error menyimpan file: " + ex.getMessage(), 
                    "Error", 
                    JOptionPane.ERROR_MESSAGE);
            }
        }
    }

    
